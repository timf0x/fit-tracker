export const getWorkoutById = (id: string): Workout | undefined => {
  return presetWorkouts.find(w => w.id === id);
};

export const getWorkoutsByLevel = (level: string): Workout[] => {
  if (level === 'all') return presetWorkouts;
  return presetWorkouts.filter(w => w.level === level || w.level === 'all');
};

export const getWorkoutsByFocus = (focus: string): Workout[] => {
  return presetWorkouts.filter(w => w.focus === focus);
};
